package Metodos;

public class CalcularMetodos {
int somar (int a, int b) {
	return a + b;
}
int somar (int a, int b, int c) {
	return a + b + c;
}
int subt (int a, int b) {
	return a - b;
}
int subt (int a, int b, int c) {
	return a - b - c;
}
int mult (int a, int b) {
	return a * b;
}
int mult (int a, int b, int c) {
	return a * b * c;
}
int div (int a, int b) {
	return a / b;
}
int div (int a, int b, int c) {
	return a / b / c;
}
}
